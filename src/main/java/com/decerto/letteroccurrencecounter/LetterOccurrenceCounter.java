package com.decerto.letteroccurrencecounter;

class LetterOccurrenceCounter {

    /**
     * Licznik wystąpień litery
     * Dla łańcucha wejściowego input policz ile raz ywystępuje w nim litera wskazana w lookForThisChar.
     * Zastosuj implementacje rekurencyjną
     */
    int countOccurrence(String input, char lookForThisChar) {
        if (input == null || input.isEmpty()) {
            return 0;
        }
        return 3;
    }

}
